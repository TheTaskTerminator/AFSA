"""FastAPI-specific code generator implementation.

This module generates FastAPI + SQLAlchemy + Pydantic code from business DSL definitions.
"""
from datetime import datetime
from typing import Any, Dict, List, Optional

from app.business.dsl import (
    APIInterface,
    BusinessModel,
    EndpointDefinition,
    FieldDefinition,
    FieldType,
    HTTPMethod,
    ParameterLocation,
    RelationshipDefinition,
    RelationshipType,
)
from app.generation.base import (
    CodeGenerator,
    CodeGeneratorRegistry,
    GeneratedFile,
    GeneratedFileType,
    GenerationContext,
)


@CodeGeneratorRegistry.register("fastapi")
class FastAPIGenerator(CodeGenerator):
    """FastAPI-specific code generator.

    Generates:
    - SQLAlchemy models
    - Pydantic schemas
    - FastAPI routers
    - Repository classes
    - Service classes
    - Alembic migrations
    """

    def generate_model(self, model: BusinessModel) -> List[GeneratedFile]:
        """Generate SQLAlchemy model and Pydantic schemas."""
        files = []

        # Generate SQLAlchemy model
        files.append(self._generate_sqlalchemy_model(model))

        # Generate Pydantic schemas
        files.extend(self._generate_pydantic_schemas(model))

        return files

    def generate_api(
        self,
        api: APIInterface,
        model: Optional[BusinessModel] = None,
    ) -> List[GeneratedFile]:
        """Generate FastAPI router with endpoints."""
        return [self._generate_router(api, model)]

    def generate_repository(self, model: BusinessModel) -> List[GeneratedFile]:
        """Generate repository/data access layer."""
        return [self._generate_repository_class(model)]

    def generate_migration(
        self,
        model: BusinessModel,
        operation: str = "create",
        revision_id: Optional[str] = None,
    ) -> List[GeneratedFile]:
        """Generate Alembic migration file."""
        return [self._generate_alembic_migration(model, operation, revision_id)]

    def generate_service(self, model: BusinessModel) -> List[GeneratedFile]:
        """Generate service layer."""
        return [self._generate_service_class(model)]

    def generate_test(
        self,
        model: BusinessModel,
        test_type: str = "unit",
    ) -> List[GeneratedFile]:
        """Generate test files for a model."""
        files = []

        if test_type in ("unit", "all"):
            files.append(self._generate_unit_tests(model))

        if test_type in ("integration", "all"):
            files.append(self._generate_integration_tests(model))

        return files

    # -----------------------------------------------------------------------
    # SQLAlchemy Model Generation
    # -----------------------------------------------------------------------

    def _generate_sqlalchemy_model(self, model: BusinessModel) -> GeneratedFile:
        """Generate SQLAlchemy model file."""
        imports = self._get_model_imports(model)
        fields_code = self._generate_model_fields(model)
        relationships_code = self._generate_model_relationships(model)
        methods_code = self._generate_model_methods(model)

        content = f'''"""SQLAlchemy model for {model.name}.

Generated by AFSA Code Generator.
"""
from datetime import datetime
from typing import Optional
from uuid import uuid4

from sqlalchemy import (
    Boolean,
    DateTime,
    Integer,
    String,
    Text,
    Float,
    Numeric,
    JSON,
    LargeBinary,
    Index,
    ForeignKey,
)
from sqlalchemy.dialects.postgresql import UUID, ARRAY
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.infrastructure.database import Base


class {model.name}(Base):
    """SQLAlchemy model for {model.name}.

    {model.description}
    """

    __tablename__ = "{model.table_name}"
{fields_code}
{relationships_code}
{methods_code}
'''

        return GeneratedFile(
            file_type=GeneratedFileType.MODEL,
            file_name=f"{model.table_name}.py",
            file_path=f"app/models/{model.table_name}.py",
            content=content,
            dependencies=["sqlalchemy>=2.0.0"],
            imports=imports,
        )

    def _get_model_imports(self, model: BusinessModel) -> List[str]:
        """Get imports needed for the model."""
        imports = [
            "from datetime import datetime",
            "from typing import Optional",
            "from sqlalchemy import Boolean, DateTime, Integer, String, Text",
            "from sqlalchemy.orm import Mapped, mapped_column, relationship",
            "from app.infrastructure.database import Base",
        ]

        # Check for UUID fields
        for field in model.fields:
            if field.field_type == FieldType.UUID:
                imports.append("from uuid import uuid4")
                imports.append("from sqlalchemy.dialects.postgresql import UUID")
                break

        return imports

    def _generate_model_fields(self, model: BusinessModel) -> str:
        """Generate SQLAlchemy field definitions."""
        fields_code = []

        for field in model.fields:
            field_code = self._generate_single_field(field)
            fields_code.append(f"    {field_code}")

        # Add indexes
        if model.indexes:
            fields_code.append("")
            fields_code.append("    __table_args__ = (")
            for idx in model.indexes:
                idx_fields = ", ".join(f'"{f}"' for f in idx.fields)
                unique_str = ", unique=True" if idx.unique else ""
                fields_code.append(
                    f'        Index("{idx.name}", {idx_fields}{unique_str}),'
                )
            fields_code.append("    )")

        return "\n".join(fields_code)

    def _generate_single_field(self, field: FieldDefinition) -> str:
        """Generate a single SQLAlchemy field definition."""
        col_type = self._get_sqlalchemy_type(field)

        args = []

        # Primary key
        if field.primary_key:
            args.append("primary_key=True")

        # Nullable
        if field.nullable or not field.required:
            args.append("nullable=True")
        else:
            args.append("nullable=False")

        # Unique
        if field.unique:
            args.append("unique=True")

        # Default
        if field.default is not None:
            if isinstance(field.default, str):
                args.append(f'default="{field.default}"')
            elif isinstance(field.default, bool):
                args.append(f"default={field.default}")
            else:
                args.append(f"default={field.default}")

        # Index
        if field.index and not field.primary_key:
            args.append("index=True")

        # Foreign key
        if field.foreign_key:
            args.append(f'ForeignKey("{field.foreign_key}")')

        args_str = ", ".join(args) if args else ""

        return f'{field.name}: Mapped[{col_type}] = mapped_column({args_str})'

    def _get_sqlalchemy_type(self, field: FieldDefinition) -> str:
        """Get SQLAlchemy type for a field."""
        type_map = {
            FieldType.STRING: "String",
            FieldType.INTEGER: "Integer",
            FieldType.FLOAT: "Float",
            FieldType.BOOLEAN: "Boolean",
            FieldType.DATETIME: "DateTime",
            FieldType.DATE: "Date",
            FieldType.TIME: "Time",
            FieldType.UUID: "UUID",
            FieldType.JSON: "JSON",
            FieldType.BINARY: "LargeBinary",
            FieldType.TEXT: "Text",
            FieldType.DECIMAL: "Numeric",
            FieldType.EMAIL: "String",
            FieldType.URL: "String",
            FieldType.PHONE: "String",
        }

        base_type = type_map.get(field.field_type, "String")

        # Add length for String types
        if base_type == "String" and field.field_type in (
            FieldType.STRING,
            FieldType.EMAIL,
            FieldType.URL,
            FieldType.PHONE,
        ):
            max_len = 255
            for v in field.validations:
                if v.rule_type == "max_length":
                    max_len = v.value
                    break
            return f"{base_type}({max_len})"

        return base_type

    def _generate_model_relationships(self, model: BusinessModel) -> str:
        """Generate SQLAlchemy relationship definitions."""
        if not model.relationships:
            return ""

        relationships = ["", "    # Relationships"]

        for rel in model.relationships:
            rel_code = self._generate_single_relationship(rel)
            relationships.append(f"    {rel_code}")

        return "\n".join(relationships)

    def _generate_single_relationship(self, rel: RelationshipDefinition) -> str:
        """Generate a single SQLAlchemy relationship definition."""
        args = [f'"{rel.target_model}"']

        if rel.back_populates:
            args.append(f'back_populates="{rel.back_populates}"')

        if rel.cascade:
            args.append(f'cascade="{rel.cascade}"')

        if rel.lazy:
            args.append(f'lazy="{rel.lazy}"')

        args_str = ", ".join(args)

        return f"{rel.name}: Mapped[{rel.target_model}] = relationship({args_str})"

    def _generate_model_methods(self, model: BusinessModel) -> str:
        """Generate model methods (to_dict, etc.)."""
        pk = model.get_primary_key()
        pk_name = pk.name if pk else "id"

        return f'''
    def to_dict(self) -> dict:
        """Convert model to dictionary."""
        return {{
            "{pk_name}": self.{pk_name},
            # Add other fields as needed
        }}

    def __repr__(self) -> str:
        return f"<{model.name}({pk_name}={{self.{pk_name}}})>"
'''

