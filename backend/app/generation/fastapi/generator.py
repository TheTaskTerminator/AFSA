        revision = revision_id or timestamp

        if operation == "create":
            upgrade_code = self._generate_migration_create(model)
            downgrade_code = self._generate_migration_drop(model)
        elif operation == "drop":
            upgrade_code = self._generate_migration_drop(model)
            downgrade_code = self._generate_migration_create(model)
